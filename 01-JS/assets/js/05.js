/* -------------------------------------------
        INCREMENTATION & DECREMENTATION
------------------------------------------- */

// ## ~ Incrémentation ~ ## //

var nb1 = 1;
nb1 = nb1 + 1;
// Ecriture simplifié
nb1++;

// ## ~ Décrémentation ~ ## //
nb1 = nb1 - 1;
// Ecriture simplifié
nb1--;

// ## ~ Subtilité ~ ## //

nb1 = 0;
console.log( nb1++ );

nb1 = 0;
console.log( ++nb1 );